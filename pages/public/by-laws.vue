            Sec. 3. Vision and Mission.
          </p>
          <p class="paragraph indent">
            3.1 VISION - Our vision of the Society is to have a strong and dynamic medical professional organization whose esteemed members are
            responsive to the needs and related concerns of our society, united in the common purpose of gaining the best possible medical knowledge
            and skills through continuing medical education and to promote the highest form of health care service for our people.
          </p>
          <p class="paragraph indent">
            3.2 MISSION - Our mission is to ensure the protection of the rights and interest of all members, promote their professional
            development and enhance recognition of the medical profession in the province of Pangasinan
