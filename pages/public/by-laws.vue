          <p class="paragraph mt-5">
            <b>PREAMBLE</b> We, the officers and members of the Pangasinan Medical Society, Inc. conscious of our duties
            and responsibilities to God, to our country and to our countrymen, imploring the aid of God Almighty,
            do hereby promulgate this Constitution and By-Laws as a declaration of our firm determination to promote
