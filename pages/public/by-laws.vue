            consistent with the nobility of our profession.
          </p>
          <p class="header">I. NAME AND OFFICE</p>
          <p class="paragraph">
            Sec. 1. Name. – The name of the organization shall be the PANGASINAN MEDICAL SOCIETY, INC.
            hereinafter referred to as the Society.
          </p>
          <p class="paragraph">
            Sec. 2. Office. – The principal office and business address of the Society shall be at the Pangasinan Medical Society, Inc
            Building located at A.B. Fernandez West Avenue, Dagupan City.
          </p>
          <p class="header">II. NATURE AND OBJECTIVES</p>
          <p class="paragraph">
            Sec. 1. Nature. – The Society is established as a non-stock, non-profit professional organization duly registered with the Securities and
            Exchange Commission under Registration Certificate No dated February 1, 1962.
          </p>
          <p class="paragraph">
            Sec. 2. Relations of the Philippine Medical Association, Inc and the Society. - The Pangasinan Medical Society, Inc. as a juridical
            entity, shall remain a component of the Philippine Medical Association, subject to the latter’s Constitution and By Laws provided that
            its provisions do not contravene the Corporation law.
          </p>
